package composition;

public class Books {

	private String Bookname;
	private String Author;
	public Books(String bookname, String author) {
		super();
		Bookname = bookname;
		Author = author;
	}
	public String getBookname() {
		return Bookname;
	}
	public String getAuthor() {
		return Author;
	}
	
	
	
}
