package operators;

public class Relational_operators {

	public static void main(String[] args) {
		
		int a=2656;
		int b=214654;
		int c=4721;
		
		
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a<b);
		System.out.println(c<a);
		System.out.println(b>=a);
		System.out.println(c<=b);
		
		
	}

}
