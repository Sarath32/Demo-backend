package primitive.A;

public class Datatype { 

	public static void main(String[] args) {
		
		byte b = 125; // 8 bits
		System.out.println("The byte value is ---> "+b);		
				
		short s = 1523; // 16 bits
		System.out.println("The short value is ---> "+s);
		
		int i = 12589; // 38 bits
		System.out.println("The integer value is ---> "+i);
		
		long l = 6382547075L; // 64 bits
		System.out.println("The long value is ---> "+l);
		
		float f = 178.5369F;
		System.out.println("The float value is ---> "+f);
		
		double d1 = 15789;
		double d2 = 15789.456797;
		System.out.println("The double value of d1 is ---> "+d1);
		System.out.println("The double value of d2 is ---> "+d2);
		
		char c='R';
		System.out.println("The character value is ---> "+c);
		
		boolean b1 = false;
		System.out.println("The boolean value is ---> "+b1);
		
		

	}

}
