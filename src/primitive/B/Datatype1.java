package primitive.B;

public class Datatype1 {
	public static void main(String[] args) {
		
		byte b = 127; // 8 bits
		System.out.println("1.The Byte Value is ---> "+b);
		
		short s = 15928; // 16 bits
		System.out.println("2.The Short Value is ---> "+s);
		
		int i = 256987; // 38 bits
		System.out.println("3.The Integer Value is ---> "+i);
		
		long l=9632468975L; // 64 bits
		System.out.println("4.The Long Value is ---> "+l);
		
		float f=1236.2365F;
		System.out.println("5.The Float Value is ---> "+f);
		
		double d=125698.2356989;
		System.out.println("6.The Double Value is ---> "+d);
		
		char c='@';
		System.out.println("7.The Character Value is ---> "+c);
		
		boolean b1= true;
		System.out.println("8.The Boolean Value is ---> "+b1);
		
		
		
	}

}
