package encapsulation;

public class Employee {

	private String Addempname;
	private String AddempAddress;
	private int AddAgeemp;
	
	public String getAddempname() {
		return Addempname;
	}
	public void setAddempname(String addempname) {
		Addempname = addempname;
	}
	public String getAddempAddress() {
		return AddempAddress;
	}
	public void setAddempAddress(String addempAddress) {
		AddempAddress = addempAddress;
	}
	public int getAddAgeemp() {
		return AddAgeemp;
	}
	public void setAddAgeemp(int addAgeemp) {
		AddAgeemp = addAgeemp;
	}
	
	
	
}
