package operatorproblemsolving;

public class Program01 {

	public static void main(String[] args) {
		
		int n=1;
		
		System.out.println(n);//1
		System.out.println(++n);//2
		System.out.println(n);//2
        System.out.println(n++);//2
        System.out.println(n);//3
        System.out.println(++n);//4
        System.out.println(--n);//3
        System.out.println(n);//3
		
		
		
	}

}
